package Week14.Exercise1;

public class Shape {
    double area;
    double volume;
    double element;
    int dimension;
    String shape;
    String elementName;

    public int getDimension(){
        return dimension;
    }

    public String getShape(){
        return shape;
    }

    public String getElementName(){
        return elementName;
    }

    public double getElement(){
        return element;
    }

    public double getArea(){
        return area;
    }


    public double getVolume(){
        return volume;
    }
}
